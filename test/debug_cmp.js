// 调试测试：检查比较运算和条件测试
let a = 2;
let b = 3;

console.log("a =", a);
console.log("b =", b);

// 测试比较表达式
let cmp = a < b;
console.log("a < b =", cmp);

// 测试 if 条件
if (a < b) {
    console.log("a < b in if: true");
} else {
    console.log("a < b in if: false");
}

// 测试反向
if (a > b) {
    console.log("a > b in if: true");
} else {
    console.log("a > b in if: false");
}
