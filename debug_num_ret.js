// Check what Number returns
var n = Number("1");
console.log("n =", n);
console.log("n + 1 =", n + 1);
console.log("n - 0 =", n - 0);
console.log("n * 1 =", n * 1);

// Now check what the literal 1 returns
console.log();
console.log("1 + 1 =", 1 + 1);
console.log("1 - 0 =", 1 - 0);

// Direct comparison
console.log();
console.log("n == 1:", n == 1);
console.log("1 == n:", 1 == n);

// What about 0 + n?
console.log();
console.log("0 + n =", 0 + n);
console.log("n + 0 =", n + 0);
